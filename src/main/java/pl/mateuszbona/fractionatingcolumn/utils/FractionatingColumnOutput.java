package pl.mateuszbona.fractionatingcolumn.utils;

public class FractionatingColumnOutput {

	private double xd;
	private double xb;

	public FractionatingColumnOutput(double xd, double xb) {
		this.xd = xd;
		this.xb = xb;
	}

	public double getXd() {
		return xd;
	}

	public double getXb() {
		return xb;
	}

}
