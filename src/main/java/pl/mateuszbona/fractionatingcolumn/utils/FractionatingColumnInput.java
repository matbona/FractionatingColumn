package pl.mateuszbona.fractionatingcolumn.utils;

public class FractionatingColumnInput {

	private double f;
	private double xf;

	public FractionatingColumnInput(double f, double xf) {
		this.f = f;
		this.xf = xf;
	}

	public double getF() {
		return f;
	}

	public double getXF() {
		return xf;
	}

}
